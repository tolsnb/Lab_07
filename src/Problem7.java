public class Problem7
{
    public static void main(String[] args)
    {
        final int LIM = 5;
        for (int row = 0; row < LIM; row++)
        {
            for (int col = 0; col < LIM; col++)
            {
                System.out.printf("*");
            }
            System.out.println("");
        }
    }
}
